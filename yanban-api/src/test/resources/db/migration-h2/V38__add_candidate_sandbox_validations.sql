CREATE TABLE candidate_sandbox_validations (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    validation_id VARCHAR(36) NOT NULL,
    user_id BIGINT NOT NULL,
    project_id BIGINT NOT NULL,
    session_id BIGINT NOT NULL,
    artifact_id BIGINT NOT NULL,
    project_version VARCHAR(64) NOT NULL,
    candidate_fingerprint VARCHAR(64) NOT NULL,
    accepted_change_indexes_json CLOB NOT NULL,
    selection_digest VARCHAR(64) NOT NULL,
    profile VARCHAR(32) NOT NULL,
    idempotency_key VARCHAR(128) NOT NULL,
    request_hash VARCHAR(64) NOT NULL,
    request_digest VARCHAR(64) NOT NULL,
    policy_digest VARCHAR(64) NOT NULL,
    request_json CLOB,
    status VARCHAR(32) NOT NULL,
    broker_execution_id VARCHAR(64),
    receipt_digest VARCHAR(64),
    receipt_json CLOB,
    error_code VARCHAR(64),
    analysis_summary CLOB,
    analysis_disclaimer VARCHAR(255),
    decision_status VARCHAR(16) NOT NULL DEFAULT 'PENDING',
    application_operation_id BIGINT,
    applied_revision_id BIGINT,
    next_attempt_at TIMESTAMP(6),
    claim_token VARCHAR(64),
    claim_expires_at TIMESTAMP(6),
    created_at TIMESTAMP(6) NOT NULL,
    updated_at TIMESTAMP(6) NOT NULL,
    CONSTRAINT uk_candidate_validation_id UNIQUE (validation_id),
    CONSTRAINT uk_candidate_validation_idempotency UNIQUE (user_id, project_id, idempotency_key),
    CONSTRAINT uk_candidate_validation_broker_execution UNIQUE (broker_execution_id)
);
CREATE INDEX idx_candidate_validation_candidate
    ON candidate_sandbox_validations(user_id, project_id, artifact_id, created_at);
CREATE INDEX idx_candidate_validation_dispatch
    ON candidate_sandbox_validations(status, next_attempt_at);
